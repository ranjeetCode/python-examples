#  Python is not “statically typed”. We do not need to declare variables before using them or declare their type.
#  A variable is created the moment we first assign a value to it.
#  A Python variable is a name given to a memory location

# A Python variable name must start with a letter or the underscore character.
# A Python variable name cannot start with a number.
# A Python variable name can only contain alphanumeric characters and underscores (A-z, 0-9, and _ ).
# Variable in Python names are case-sensitive (name, Name, and NAME are three different variables).
# The reserved words(keywords) in Python cannot be used to name the variable in Python.

integer = 7
float_num = 2.718
bool_val = False
print(integer, float_num, bool_val)
integer = 10
print(integer, float_num, bool_val)
