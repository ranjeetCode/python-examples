# Expressions and Basic Math Operators
addition = 4 + 5    # 9
subtraction = 5 - 2 # 3
division = 7 / 2    # 3.5
multiplication = 3 * 8  # 24
exponentiation = 4 ** 4 # Exponentiation 4 x 4 x 4 x 4 = 256
floor_division = 16 // 5    # 3 (Floor division,  It will only store the integer part)
modulo = 7 % 3  # 1

add_assign = 5
add_assign = add_assign + 5
add_assign += 7


print(addition, subtraction, multiplication, division, exponentiation, floor_division, modulo, add_assign)
