# round() function returns a floating point number that is a rounded version of the specified number,
# with specified number of decimal.
# The default number of decimal is 0, meaning the function will return the nearest number

# Syntax
    # round(number, digit)
    # The first argument is required and it can be number/expression/variable and the second argument is optional.

expression1 = 1.9548 + 2.8078
print(round(expression1, 2))
